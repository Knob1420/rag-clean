# ════════════════════════════════════════════════════════════

DOC_INFO_SYSTEM_PROMPT = "你是一个企业文档分类专家，擅长提取结构化元数据。只返回纯 JSON，不要 markdown 格式符号。"


def build_doc_info_prompt(title: str, content_preview: str) -> str:
    """构建文档信息提取 prompt"""
    return f"""你是一个企业文档分类专家。请分析以下文档，提取结构化元数据。

文档标题：{title}
文档内容预览：
{content_preview}

请以 JSON 格式返回以下字段：
{{
    "doc_type": "文档体裁",
    "domain": "业务领域",
    "entities": {{"实体类型": "实体值"}},
    "filter_terms": ["专有名词1", "专有名词2"],
    "topics": ["主题1", "主题2"],
    "doc_intent": "一句话概括文档目的",
    "summary": "150-300字自解释摘要",
    "confidence": 90
}}

═══ 各字段规则 ═══

【1. doc_type】文档体裁（细粒度，决定 ingestion 处理策略）：
根据文档内容选择最贴切的类型，不限于以下选项：
- 产品手册：产品或设备的使用说明、技术参数文档
- 合同协议：合作协议、采购合同、保密协议等法律文件
- 技术文档：接口文档、技术架构说明、开发指南
- 解决方案/白皮书：行业解决方案、技术白皮书、最佳实践
- 规章制度：公司政策、流程规范、行政通知、组织公告
- FAQ/知识库：常见问题、故障排除、知识问答
- 宣传画册：综合产品画册、项目宣传材料、企业介绍
- 其他：无法归类的文档

【2. domain】业务领域（粗粒度，4 选 1，用于检索路由）：
必须且只能从以下 4 个中选择一个：
- Product_Tech：产品与技术（手册、规格书、研发文档、技术方案、测试报告）
- Biz_Market：业务与市场（宣传册、售前方案、竞品分析、报价单、画册）
- Ops_Mgmt：运营与管理（项目管理、流程规范、会议纪要、工作计划）
- Corp_Support：企业与职能（HR 制度、财务报销、行政通知、组织公告）

判断方法：看文档的核心内容是"讲产品"还是"管流程"还是"做业务"。

【3. entities】文档级实体（灵活 KV，ingestion 上下文传递）：
- 提取文档中反复出现的核心实体（参与方、产品、组织、项目等）
- 先判断 doc_type，再从对应模板中选择 key，不得自创 key：
  产品手册 → 必填: 品牌, 品类, 产品型号; 可选: 系列, 子型号
  合同协议 → 必填: 甲方, 乙方; 可选: 合同编号, 合同类型, 项目名称, 签订日期
  技术文档 → 必填: 技术领域; 可选: 文档用途, 框架, 版本, 平台
  解决方案/白皮书 → 可选: 行业, 方案类型, 适用场景, 客户
  规章制度 → 可选: 制度名称, 适用部门, 发布日期, 制度编号
  FAQ/知识库 → 可选: 主题, 适用产品, 问题类型
  宣传画册 → 必填: 品牌; 可选: 涉及产品, 涉及项目
- 不应包含：具体技术参数（算力、重量、功耗等），这些属于 chunk 级别

【4. filter_terms】硬过滤专有名词（平铺列表，用于检索精确过滤）：
- 只提取专有名词：产品名、项目名、公司名、人名、品牌名
- 不包含通用词：如"路由器"、"手册"、"技术文档"、"解决方案"
- 这些词将用于 Elasticsearch keyword 字段的精确匹配
- 示例：["G1", "智加", "三体计算星座", "NX1"]

【5. topics】文档涉及的主题（2-5 个）：
- 从全文视角抽象出文档覆盖了哪些主题领域
- 示例：["技术参数", "使用指南", "散热设计"]

【6. doc_intent】文档意图（一句话）：
- 概括这篇文档"为什么要写"或"写给谁看"
- 示例："向客户介绍 G1 星载智算机的技术特点和应用场景"

【7. summary】文档全局摘要（150-300 字）：
- 必须是自解释文本：不看原文档也能理解文档是关于什么的
- 必须包含：文档核心主题、涉及的产品/项目、关键结论或价值点
- 写给谁看：面向检索路由系统，用于比对用户提问与文档主旨

【8. confidence】置信度（0-100）：
- 评估你对以上所有字段提取结果的把握程度
- 内容模糊、跨多个领域、类型难以判断时给低分（< 70）

═══ 标题解析注意 ═══
- 标题中可能包含日期格式的数字（如 251203），它们是日期而非实体的一部分
- 正确：'智加NX1'，错误：'智加NX1-251203'

只返回 JSON，不要其他内容。"""


