def build_react_step_prompt(
    original_query: str,
    accumulated_summary: str,
    summarized_results: str,
    hop_number: int,
    max_hops: int,
) -> str:
    """构建每步推理的 user prompt"""
    remaining = max_hops - hop_number
    return f"""## 原始问题
{original_query}

## 已累积的发现摘要
{accumulated_summary}

## 本轮检索结果
{summarized_results}

## 推理轮次
第 {hop_number} 轮（剩余 {remaining} 轮）

## 关键要求
1. **识别产品实体**：从检索结果中提取具体的产品型号（如"智加G1"、"NX3"）
2. **更新 search_entities**：将发现的具体型号加入列表，用于后续精确检索
3. **更新 search_keywords**：用具体产品名替换模糊的类别词（如用"智加G1"替换"国产智算机"）
4. **聚焦信息缺口**：如果上一步发现了产品列表，下一步应获取具体规格参数

请分析上述信息，决定下一步行动。如果已有足够信息回答问题，action=finish。
必须严格输出 JSON 格式。"""
