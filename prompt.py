CHUNK_INFO_SYSTEM_PROMPT = "你是一个文档分析专家，擅长提取结构化信息。只返回JSON格式。"


def build_chunk_info_prompt(
    chunks_content: List[str],
    doc_context: dict,
    num_chunks: int,
) -> str:
    """构建 Chunk 信息批量提取 prompt"""
    return f"""你是一个文档分析专家。你的任务是为以下 {num_chunks} 个文档片段（Chunk）提取用于 RAG 检索的高质量结构化元数据。

【全局文档上下文】（请将此信息作为每个片段的默认背景）：
- 文档类型：{doc_context.get('doc_type', '未知')}
- 文档标题：{doc_context.get('title', '未知')}
- 全局实体：{doc_context.get('global_entities', {})}

【待处理的文档片段】：
{chr(10).join(chunks_content)}

【输出格式】
请严格按照以下 JSON 格式输出，**必须且只能包含 {num_chunks} 个元素**，不得遗漏任何一个片段！

{{
    "chunks":[
        {{
            "index": 0,
            "section_title": "简洁章节标题",
            "chunk_type": "片段类型",
            "keywords": ["关键词1", "关键词2", "关键词3"],
            "context_summary": "语义检索摘要"
        }}
    ]
}}

【字段规则】

1. section_title（章节标题，2-10个字符）：
   - 结合 chunk 内容和提供的原标题，由 LLM 综合提炼一个最恰当的标题
   - 如果原标题已准确概括内容，可直接沿用或微调
   - 如果是合并后的 chunk（包含多个原始小节），需要综合各部分内容提炼更合适的标题
   - 应该简洁、准确，能概括本 chunk 的核心主题
   - 避免过于宽泛（如"产品介绍"）或过于细节（如具体参数值）

2. chunk_type（内容功能标签，只能从以下 7 种中选择一种）：
   根据片段内容承担的**文本功能**选择标签，而非按主题分类：
   - intro: 概述、背景、概念介绍（"是什么"）
   - spec_data: 参数表、测试数据、性能指标、数值规格（"具体数值"）
   - feature: 优势、特性、核心竞争力、对比亮点（"好在哪"）
   - procedure: 步骤、指南、流程、操作说明、方法（"怎么做"）
   - faq: 问题解答、故障排查、注意事项（"怎么办"）
   - profile: 团队介绍、公司资质、案例名录、参与方信息（"是谁"）
   - other: 目录、版权声明、免责声明等无独立检索价值的内容
   注意：不管文档类型是产品手册还是合同，任何片段的功能都逃不出这 7 种。
   具体讲什么主题由 keywords 负责。

2. keywords（检索关键词，3-5个）：
   - 提取用户在搜索框中最可能输入的词
   - 优先提取：专业术语、独特的命名、关键实体名称
   - 避免：停用词

3. context_summary（向量检索摘要，极其重要！）：
   - 结构要求：必须是包含主语的完整陈述句
   - 内容要求：必须包含本片段最核心的 1-2 个具体信息点

请确保返回合法 JSON 字符串，不要包含 markdown 格式符号（如 ```json），直接输出 JSON 内容。"""


