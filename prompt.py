QUERY_REWRITE_SYSTEM_PROMPT = "你是一个专业的查询理解与重写专家。分析用户查询，输出结构化JSON用于驱动检索系统。只返回JSON格式。"


def build_rewrite_prompt(
    query: str, chunk_types_list: str
) -> str:
    """构建查询重写 prompt（动态注入 ES chunk_type 列表）"""
    return f"""分析用户查询，输出结构化 JSON。

## 知识库内容类型
用户查询的内容可能属于以下 chunk_type：
[{chunk_types_list}]

## 输出字段说明

### intent_type
从上面的 chunk_type 列表中选择最匹配的一个或多个（逗号分隔）。这个值会用于检索时的类型加权，帮助优先返回相关类型的内容。如果都不匹配，返回 "other"。

### target_entities
提取查询中的关键实体（产品型号、部门、项目名、系统名等）。无则返回空数组。

### rewritten_query
重写后的完整自然语言问句，用于语义检索。
- 主谓宾完整、通顺（严禁堆砌关键词）
- 补全缺失的主语和上下文
- **必须保留原始查询中的所有约束条件**（数量、属性、意图词等）

### keywords
用于全文精准匹配的关键词，补充 2-3 个同义词。

### strategy — 检索路由策略
- **direct**: 单一意图，一次检索即可回答
- **parallel**: 可分解为多个独立子查询，并行检索更高效（如多实体对比、多角度查询）
- **sequential**: 需要多步推理，前一步结果决定后一步方向（如先列举再筛选、先查找再推荐）

### sub_queries
仅 strategy 为 parallel 或 sequential 时需要。每个子查询包含：
- query: 子查询语句
- entity: 目标实体
- intent: 匹配的 chunk_type

## 原始查询
{query}

## 输出格式（严格 JSON）
{{
    "intent_type": "匹配的chunk_type",
    "target_entities": ["实体1"],
    "keywords": ["词1", "词2", "词3"],
    "rewritten_query": "重写后的完整问句（保留全部约束）",
    "strategy": "direct 或 parallel 或 sequential",
    "sub_queries": []
}}

## 示例

**示例 1：直接检索**
输入: "报销流程是什么？"
输出: {{
    "intent_type": "procedure",
    "target_entities": [],
    "keywords": ["报销", "流程", "费用报销", "出差报销"],
    "rewritten_query": "公司员工的费用报销流程和具体步骤是什么？",
    "strategy": "direct",
    "sub_queries": []
}}

**示例 2：多实体对比（parallel）**
输入: "智加G3和智加NX3有什么区别？"
输出: {{
    "intent_type": "feature",
    "target_entities": ["智加G3", "智加NX3"],
    "keywords": ["智加G3", "智加NX3", "对比", "区别", "差异"],
    "rewritten_query": "智加G3和智加NX3在功能特点和规格参数上有哪些差异？",
    "strategy": "parallel",
    "sub_queries": [
        {{"query": "智加G3的功能特点和规格参数", "entity": "智加G3", "intent": "feature"}},
        {{"query": "智加NX3的功能特点和规格参数", "entity": "智加NX3", "intent": "feature"}}
    ]
}}

**示例 3：约束过滤+推荐（sequential）**
输入: "3kg以内的星载智算机，可以帮我推荐一个吗？"
输出: {{
    "intent_type": "spec_data",
    "target_entities": ["星载智算机"],
    "keywords": ["星载智算机", "重量", "3kg", "型号", "推荐"],
    "rewritten_query": "3kg以内的星载智算机有哪些型号？重量在3kg以下的星载智算机推荐",
    "strategy": "sequential",
    "sub_queries": [
        {{"query": "星载智算机有哪些型号？", "entity": "星载智算机", "intent": "spec_data"}},
        {{"query": "各型号星载智算机的重量参数", "entity": "星载智算机", "intent": "spec_data"}}
    ]
}}

请针对用户的【原始查询】只返回合法的 JSON，不要输出任何其他内容。"""


